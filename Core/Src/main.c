void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
    if (huart->Instance == USART3)
    {

        // --- PROTOCOL STATE MACHINE ---

        // 1. Waiting for Start of Packet
        if (rx_idx == 0)
        {
            if (rx_byte_tmp == STX)
            {
                rx_buffer[rx_idx++] = rx_byte_tmp;
            }
        }
        // 2. Receiving Payload
        else if (rx_idx < (PACKET_SIZE - 1))
        {
            rx_buffer[rx_idx++] = rx_byte_tmp;
        }
        // 3. Waiting for End of Packet
        else if (rx_idx == (PACKET_SIZE - 1))
        {
            if (rx_byte_tmp == ETX)
            {
                rx_buffer[rx_idx] = rx_byte_tmp;
                packet_received = 1; // Complete valid packet received!
            }

            // Whether valid or not, reset index to look for next STX
            rx_idx = 0;
        }

        // Re-arm interrupt for next byte
        HAL_UART_Receive_IT(&huart3, &rx_byte_tmp, 1);
    }
}

// --- LCD FUNCTIONS (Kept mostly same, added robust updates) ---

void lcd_send_nibble(uint8_t nibble)
{
    HAL_I2C_Master_Transmit(&hi2c3, LCD_ADDR, &nibble, 1, 10);
}

void lcd_send_byte(uint8_t data, uint8_t rs)
{
    uint8_t high = (data & 0xF0) | LCD_BACKLIGHT | rs;
    uint8_t low  = ((data << 4) & 0xF0) | LCD_BACKLIGHT | rs;

    lcd_send_nibble(high | LCD_ENABLE);
    lcd_send_nibble(high);

    lcd_send_nibble(low | LCD_ENABLE);
    lcd_send_nibble(low);

    HAL_Delay(1); // Small delay for LCD processing
}

void lcd_cmd(uint8_t cmd)
{
    lcd_send_byte(cmd, 0);
}

void lcd_data(uint8_t data)
{
    lcd_send_byte(data, LCD_RS);
}

void lcd_init(void)
{
    HAL_Delay(50);
    lcd_cmd(0x33);
    lcd_cmd(0x32);
    lcd_cmd(0x28); // 4-bit mode, 2 lines (physically 4 lines usually treated as 2 banks)
    lcd_cmd(0x0C); // Display ON, Cursor OFF
    lcd_cmd(0x06); // Increment cursor
    lcd_cmd(0x01); // Clear display
    HAL_Delay(5);
}

void lcd_print(char *str)
{
    while (*str)
    {
        lcd_data(*str++);
    }
}

void lcd_set_cursor(uint8_t row, uint8_t col)
{
    // Standard 20x4 addresses
    // Row 0: 0x00
    // Row 1: 0x40
    // Row 2: 0x14
    // Row 3: 0x54
    uint8_t address[] = {0x00, 0x40, 0x14, 0x54};
    if(row > 3) row = 3;
    lcd_cmd(0x80 | (address[row] + col));
}

// Helper to update a specific line
void lcd_update_line(uint8_t row, const char *raw_data_start)
{
    lcd_set_cursor(row, 0);
    // Copy exactly 20 chars to a temp buffer to ensure null termination
    memcpy(display_line_buf, raw_data_start, 20);
    display_line_buf[20] = '\0';
    lcd_print(display_line_buf);
}

void uart_println(char *msg)
{
    HAL_UART_Transmit(&huart3, (uint8_t*)msg, strlen(msg), 100);
    HAL_UART_Transmit(&huart3, (uint8_t*)"\r\n", 2, 100);
}

